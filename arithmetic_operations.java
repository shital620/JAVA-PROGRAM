public class arithmetic_operations {
    
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(a/b);
        System.out.println(++a);
        System.out.println(--a);
    }
}

/* OUTPUT
 30
-10
200
10
0
11
10
 
*/
